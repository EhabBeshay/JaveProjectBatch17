package Review3;

import java.util.Scanner;

public class E7DoWhile {
    public static void main(String[] args) {
        Scanner s=new Scanner(System.in);
        int x=0;
        int sum=0;
        do {
            System.out.println("Please enter the number -1 to terminate");
            x=s.nextInt();
            if (x!=-1);
            sum=sum+x;
        } while (false);
    }
}
