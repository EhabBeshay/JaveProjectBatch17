public class HW3 {
    public static void main(String[] args) {
        for (int i=20; i>=2; i-=2){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("Another Loop");
        int i=20;
        while (i>=2){
            System.out.print(i+" ");
            i-=2;
        }
    }
}
