package Class3;

public class E9ElseIfStatment {
    public static void main(String[] args) {
        int day = 1;

        if (day==1){
            System.out.println("Its Monday");
        } else if (day==2) {
            System.out.println("Its Tuesday");

        } else if (day==3) {
            System.out.println("Its Wednesday");
        } else if (day==4) {
            System.out.println("Its thursday");
        }
    }
}
